package einkaufsliste;

public class Rezept {
    int rezeptID;
    int anzahl;


    public int getRezeptID() {
        return this.rezeptID;
    }

    public void setRezeptID(int rezeptID) {
        this.rezeptID = rezeptID;
    }

    public int getAnzahl() {
        return this.anzahl;
    }

    public void setAnzahl(int anzahl) {
        this.anzahl = anzahl;
    }

}
